function maior(num1,num2,num3) {
    if (num1>num2 && num1>num3)
        console.log(`O número ${num1} é maior`)
    else if (num2>num3)
        console.log(`O número ${num2} é maior`)
    else
        console.log(`O número ${num3} é maior`)
}

maior(23,30,77)
maior(50,18,37)
maior(22,45,21)
maior(40,11,73)